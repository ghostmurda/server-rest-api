let getUsersList = function (skip, pageSize){
    let newArr = [];
    let i = 0;
    let j = skip * pageSize;
    while (i < pageSize)
    {
        newArr.push(usersList[j]);
        i++;
        j++;
    }
    return newArr;
}

let getUser = function(userId){
    let user = {};
    for (let userItem of usersList){
        if (userItem.id == userId){
            user = userItem;
            break;
        }
    }
    user.posts = user.postsData.length;
    user.postsData.sort((a, b) => b.id - a.id );
    return user;
}

let getFollowingList = function (userId){
    let followingList = [];
    for (let userItem of usersList){
        if (userItem.id == userId){
            followingList = userItem.followingList;
            break;
        }
    }
    return followingList;
}

